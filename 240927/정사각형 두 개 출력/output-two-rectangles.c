#include <stdio.h>

int main() 
{
    int n;
    scanf("%d", &n);

    for(int i = 0; i < n; i++)
    {
        int j;
        for(j = 0; j < n; j++)
        {
            printf("*");
        }

        if(j = n) printf("\n");
    }

    printf("\n");
    
    for(int i = 0; i < n; i++)
    {
        int j;
        for(j = 0; j < n; j++)
        {
            printf("*");
        }

        if(j = n) printf("\n");
    }
    return 0;
}